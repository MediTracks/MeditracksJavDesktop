    private void jLabel7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel7MouseClicked
        try {
            donnee.setServerName(Txt_Serveur.getText());
            donnee.setServerUser(Txt_User.getText());
            donnee.setServerPassword(Txt_Password.getText());
            donnee.setConfirPassword(Txt_Confirm_Password.getText());
            donnee.setDatabaseName(Txt_DB.getText());
            if (donnee.getServerPassword() == null ? donnee.getConfirPassword() != null : !donnee.getServerPassword().equals(donnee.getConfirPassword())) {
                JOptionPane.showMessageDialog(this, "Les deux mots de passe ne sont pas identiques", "Configuration", JOptionPane.WARNING_MESSAGE);
            } else {
                if (donnee.Configuration()) {
                    Txt_DB.setText("");
                    Txt_User.setText("");
                    Txt_Serveur.setText("");
                    Txt_Password.setText("");
                    Txt_Confirm_Password.setText("");
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Configuration", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Configuration", JOptionPane.ERROR_MESSAGE);
        }
        this.dispose();
    }//GEN-LAST:event_jLabel7MouseClicked

